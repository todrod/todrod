const goalKeywordMap: Record<string, string[]> = {
  "medical-office": ["medical", "clinic", "healthcare"],
  "saas-app": ["saas", "general"],
  "landing": ["landing", "service landing"],
  "ecommerce": ["ecommerce"],
  "portfolio": ["portfolio"],
  "blog-docs": ["documentation", "knowledge"],
  "podcast": ["podcast"],
  "agency": ["agency", "creative"],
  "restaurant": ["restaurant", "food"],
  "nonprofit": ["nonprofit", "charity"],
  "event": ["event management"],
  "real-estate": ["real-estate", "property"],
  "course-platform": ["course", "e-learning"],
  "exam-module": ["education", "learning"],
};

export function getProductRecForGoal(goalId: string): ProductStyleRec | undefined {
  const goalTerms = goalKeywordMap[goalId];
  if (!goalTerms) return undefined;

  // Special case: ecommerce should NOT match luxury
  if (goalId === "ecommerce") {
    return productStyleRecs.find(
      (rec) =>
        rec.keywords.some((kw) => goalTerms.includes(kw)) &&
        !rec.keywords.includes("luxury"),
    );
  }

  return productStyleRecs.find((rec) =>
    goalTerms.some((term) =>
      rec.keywords.some((kw) => kw.includes(term) || term.includes(kw)),
    ),
  );
}
