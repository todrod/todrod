const severityOrder: Record<UXGuideline["severity"], number> = {
  Critical: 0,
  High: 1,
  Medium: 2,
  Low: 3,
};

export function getGuidelinesForContext(
  layoutType?: string,
  mustHaves?: string[],
): UXGuideline[] {
  const alwaysIncludeCategories = new Set(["Navigation", "Layout", "Responsive", "Animation", "Accessibility"]);
  const includeForms = mustHaves?.includes("forms") ?? false;

  const filtered = uxGuidelines.filter((g) => {
    if (alwaysIncludeCategories.has(g.category)) return true;
    if (includeForms && g.category === "Forms") return true;
    return false;
  });

  const sorted = [...filtered].sort(
    (a, b) => severityOrder[a.severity] - severityOrder[b.severity],
  );

  return sorted.slice(0, 5);
}
