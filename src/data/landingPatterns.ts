export function getLandingPatternForGoal(goalId: string): LandingPattern | undefined {
  const terms = goalToPatternKeywords[goalId];
  if (!terms) return undefined;
  return landingPatterns.find((pattern) =>
    terms.some((term) =>
      pattern.keywords.some((kw) => kw.includes(term) || term.includes(kw)),
    ),
  );
}
