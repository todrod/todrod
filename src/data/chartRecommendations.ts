// The 6 most common chart types for dashboards in order:
// Trend, Compare, Part-to-Whole, Performance, Funnel, Time-Series Forecast
const TOP_DASHBOARD_IDS = [
  "trend-over-time",
  "compare-categories",
  "part-to-whole",
  "performance-vs-target",
  "funnel-flow",
  "time-series-forecast",
];

export function getTopChartsForDashboard(): ChartRec[] {
  return TOP_DASHBOARD_IDS.map((id) => chartRecs.find((r) => r.id === id)).filter(
    (r): r is ChartRec => r !== undefined,
  );
}
